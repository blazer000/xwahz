                </div>
                <div class="cs">
                    <span>专家审稿</span>
                    <p>由编辑部负责稿件的初审工作，在严谨的审查之后，通过初审的稿件提交至专家进行专家审稿阶段。 </p>
                </div>
                <div class="fb">
                    <span>稿件复审</span>
                    <p>专家审稿完毕后，交由总编进行复审，在严谨的审查之后，通过复审的稿件会提交至编辑部进行定稿。 </p>
                </div>
                <div class="fs">
                    <span>主编定稿</span>
                    <p>定稿之后，本刊编辑部会进行邮件以及电话通知发稿人，并在期刊发行之后邮寄样刊。</p>
